/*
    Scrum é um framework para gestão e planejamento de projetos.

    No scrum, os projetos são divididos em ciclos chamados de sprints.
    Os sprints representa um espaço de tempo, normalmente 
    de 2 a 4 semanas, no qual um conjunto de atividades deve ser executado.

    As funcionalidades a serem implementadas no projeto são 
    mantidas em umas lista chamada Product Backlog.

    O Product Backlog é uma lista contendo todas as funcionalidades desejadas para um produto.
    O product owner prioriza os itens do Product Backlog e os descreve para a equipe.
    A equipe determina que itens será capaz de completar durante a sprint que está começando.
    Os itens que foram escolhidos são transferidos para o sprint backlog.

    A cada dia de sprint, a equipe faz uma breve reunião chamada Daily meeting 
    ou daily scrum, que tem duração de no máximo 15 min, com o objetivo de falar sobre o que foi feito ontem, identificar impedimentos 
    e priorizar o trabalho do dia que se inicia.

    Ao final do sprint, a equipe apresenta as funcionalidades implementadas em uma sprint review meeting.
    Também é feito uma sprint retro meeting ou Sprint Retrospective, onde são apontados 
    os pontos positivos e negativos que ocorreram e determinam 
    ações para avitar os pontos negativos novamente.
*/